from langchain_core.messages import AIMessage, HumanMessage, SystemMessage


from datetime import date as date_type


def build_full_prompt(
    tours_text: str,
    faq_context: str,
    history: list[dict],
    message: str,
    escalation_count: int = 0,
    current_date: date_type | None = None,
    current_time: str | None = None,
) -> list[SystemMessage | HumanMessage | AIMessage]:
    system = _build_system(tours_text, faq_context, escalation_count, current_date, current_time)
    messages: list[SystemMessage | HumanMessage | AIMessage] = [
        SystemMessage(content=system)
    ]
    for h in history:
        if h["role"] == "user":
            messages.append(HumanMessage(content=h["content"]))
        else:
            messages.append(AIMessage(content=h["content"]))
    messages.append(HumanMessage(content=message))
    return messages


def _build_system(
    tours_text: str, faq_context: str, escalation_count: int = 0,
    current_date: date_type | None = None, current_time: str | None = None,
) -> str:
    today = (current_date or date_type.today()).strftime("%d.%m.%Y")
    now = current_time or "неизвестно"
    parts = [
        _BASE_RULES.format(today=today, current_time=now),
        _SECURITY_RULES,
    ]

    if tours_text:
        parts.append(_TOURS_HEADER + tours_text)

    if faq_context:
        parts.append(_FAQ_HEADER + faq_context)

    parts.append(_ACTION_INSTRUCTIONS)
    if escalation_count < 3:
        parts.append(_ESCALATION_RULES)
    else:
        parts.append(_ESCALATION_LIMIT_REACHED)

    return "\n\n".join(parts)


